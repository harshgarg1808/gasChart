  getData(data: any , fromDate : any , toDate: any){

    var objArr : any = [];

    for(var i = 0 ; i<data.length ; i++){

      if(data[i].x >= parseInt(fromDate) && data[i].x <= parseInt(toDate) ){
        objArr.push(data[i]);
      }
    }
    console.log(objArr);

    var sortedArray: { x: number, y : number; }[] = objArr.sort((n1 : any ,n2 : any) => {
      if (n1.x > n2.x) {
          return 1;
      }
  
      if (n1.x < n2.x) {
          return -1;
      }
  
      return 0;
    });    

    return sortedArray;
  }
  
